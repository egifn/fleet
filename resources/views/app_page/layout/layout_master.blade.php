    <script>
        // DELETE ITEMS
        $(document).on("click", "#delitems", function(e) {
            e.preventDefault();
            var targetUrl = $(this).attr("href");
            var id = $(this).attr("id");
            Swal.fire({
                title: 'Apakah kamu yakin?',
                text: "ini akan Menghapus semua data",
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Yes, delete it!'
            }).then((result) => {
                if (result.value) {
                    var postdata = {}
                    postdata["id"] = id;
                    $.get(targetUrl, postdata, function(result) {
                        location.reload();
                    });
                }
            });
        });
    </script>
